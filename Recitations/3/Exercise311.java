class Exercise311 {
    public static void main(String[] args) {
        /*
         * Use a for loop to print the values 1 to n. Note: you should set n to be a number of your choice. 
         * (Clarification, n should be set in main, rather than be given as a command-line argument.)  
         * For example, for n=5 
         * 1 
         * 2 
         * 3 
         * 4 
         * 5 
         */
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
