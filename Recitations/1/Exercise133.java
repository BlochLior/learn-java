class Exercise133 {
    public static void main(String[] args) {
        // Write a Java program to calculate and print the area of a circle.
        // pi * r * r
        double pi = 3.14159;
        int r = 2;
        double circleArea = pi * r * r;
        System.out.println("The area of a circle with a radius of " + r + " is: " + circleArea);
    }
}